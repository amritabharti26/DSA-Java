package basicOfJava;

public class arithmeticOperator {
    public static void main(String[] args) {
        int x = 12, y = 100; //int operation give int no float value
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);

        System.out.println("for float");
        float u = 12, v = 100; //int operation give int no float value
        System.out.println(u+v);
        System.out.println(u-v);
        System.out.println(u*v);
        System.out.println(u/v);
        System.out.println(u%v);

        System.out.println("for double");
        double a = 12, b = 100; //int operation give int no float value
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
