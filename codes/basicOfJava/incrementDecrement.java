package basicOfJava;

public class incrementDecrement {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(x);
        x += 6;
        System.out.println(x);
        x -= 4;
        System.out.println(x);
        x *= 6;
        System.out.println(x);

        int y = 5;
        y++;
        System.out.println(y);
    }
}
